// Operadores de comparação maior e menor (igual)

let one = 1
let two = 2

// >    Maior que
console.log(one > two)  //false


// >=   Maior igual a   (maior ou igual)
console.log(one >= 1)  //true
console.log(two >= 1)


// <    Menor que
console.log(one < two)


// <=   Menor igual a   (menor ou igual)
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)

