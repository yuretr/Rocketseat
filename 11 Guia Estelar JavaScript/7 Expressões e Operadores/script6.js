// Operadores de comparação igual a e diferente de

// Irá comparar valores e retornar um Boolean como resposta à comparação
//false or true

let one = 1
let two = 2

// ==   igual a
console.log(two == 1)
console.log(one == "1")

// !=   diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != "1")


// = (recebe, atribuido, por exemplo one recebe 1)
// == (igual)
