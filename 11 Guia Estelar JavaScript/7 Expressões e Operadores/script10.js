// Operadores lógicos (logical operators)

// 2 valores boleanos, quando verificados,
// resultada em verdade ou falso

let pao = false
let queijo = true

// AND &&       ("e")
console.log(pao && queijo)
// somente se os dois são true, ele dará true

// QR ||        ("ou")
console.log(pao || queijo)
// se pelo menos um for true, ele resultará em true

// NOT !        ("não")
console.log(!pao)
console.log(!queijo)
// ele nega o valor, inverte e troca de valor

