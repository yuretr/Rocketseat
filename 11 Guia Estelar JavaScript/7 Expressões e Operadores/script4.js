// Operadores aritméticos

// multiplicação *
console.log(3.2 * 5)

// divisão /
console.log(12 / 2)

// soma +
console.log(34 + 67)

// subtração -
console.log(34 - 23)

// resto da divisão %
let remainder
remainder = 11 % 3
console.log(remainder)


// incremento ++
let increment = 0
increment++  // aqui adiciona +1
increment++  // aqui adiciona +1 novamente
console.log(increment)

console.log(increment++)  // incrementa após, então agora adicionou mais um, mas não vai aparecer o resultado, somente no próximo, veja abaixo
console.log(increment)  // resultado

console.log(++increment) // incrementa antes de apresentar o resultado


// decremento --
let decrement = 0
console.log(--decrement)  // mesmas configurações do incremento

// exponencial **
console.log(3 ** 3)
console.log(3 ** 2)


