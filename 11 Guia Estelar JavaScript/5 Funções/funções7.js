// Arrow function

// Também podemos escrever funções expression de uma maneira mais compacta, desta maneira: () => { código a ser executado }, chamada de Arrow Function.

const sayName = () => {
    console.log('Yurezão')
}

sayName()

// como é variável, tem que chamar depois que declarada a função

// função compacta, pode ser escrita nesse formarto "() => {}"

